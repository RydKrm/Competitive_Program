#include<iostream>
using namespace std;
int ans[200000];
int main(){
    int sum = 0;
    for(int i=1;i<=200000;i++){
        int cnt = 0,n=i;
        while (n){
           cnt += n%10;
           n /= 10;
        }
        sum += cnt;
        ans[i] = sum;
    }

    int ts;cin>>ts;
    while(ts--){
       int n;cin>>n;
       cout<<ans[n]<<endl;
    }
} 

